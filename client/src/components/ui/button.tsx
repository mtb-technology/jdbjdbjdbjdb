import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-lg text-sm font-medium ring-offset-background transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        // Primary: De koningsblauwe knop zoals "Aan de slag" op de website
        default: "bg-primary text-primary-foreground shadow-sm hover:bg-jdb-blue-hover active:scale-[0.98]",
        // Destructive: Rood voor gevaarlijke acties
        destructive:
          "bg-destructive text-destructive-foreground shadow-sm hover:bg-destructive/90 active:scale-[0.98]",
        // Outline: Transparant met blauwe border zoals "Direct belastingadvies"
        outline:
          "border-2 border-primary bg-transparent text-primary hover:bg-jdb-blue-light active:scale-[0.98]",
        // Secondary: Lichtgrijs voor minder prominente acties
        secondary:
          "bg-secondary text-secondary-foreground border border-border hover:bg-muted active:scale-[0.98]",
        // Ghost: Minimale styling, alleen hover effect
        ghost: "hover:bg-jdb-blue-light hover:text-primary",
        // Link: Text-only knop
        link: "text-primary underline-offset-4 hover:underline",
      },
      size: {
        default: "h-11 px-6 py-2.5",        // Iets groter voor professionele look
        sm: "h-9 px-4 py-2 text-xs",
        lg: "h-12 px-8 py-3 text-base",    // Prominente CTA buttons
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
